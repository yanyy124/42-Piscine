#include <unistd.h>

int	main(int ac, char *argv[])
{
	int	i;
	int	j;

	i = 1;
	if (ac > 1)
	{
		ac--;
		while (ac >= i)
		{
			j = 0;
			while (argv[ac][j])
			{
				write(1, &argv[ac][j], 1);
				j++;
			}
			write(1, "\n", 1);
			ac--;
		}
	}
	return (0);
}
